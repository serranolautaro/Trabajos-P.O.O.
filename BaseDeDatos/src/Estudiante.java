
public class Estudiante {
		private String nombre;
		private int legajo;
		
		public String getNombre() {
			return nombre;
		}
		public void setNombre(String nombre) {
			this.nombre = nombre;
		}
		public int getLegajo() {
			return legajo;
		}
		public void setLegajo(int legajo) {
			this.legajo = legajo;
		}

}
