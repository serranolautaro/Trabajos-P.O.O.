
public class Simpatizantes {
	private boolean problemasVocales;
	private int preferenciaBauza;
	private double nivelConsumoNesquick;
	private double maximoTolerancia = 100;
	
	
	public boolean isProblemasVocales() {
		return problemasVocales;
	}
	public void setProblemasVocales(boolean problemasVocales) {
		this.problemasVocales = problemasVocales;
	}
	public int getPreferenciaBauza() {
		return preferenciaBauza;
	}
	public void setPreferenciaBauza(int preferenciaBauza) {
		this.preferenciaBauza = preferenciaBauza;
	}
	public double getNivelConsumoNesquick() {
		return nivelConsumoNesquick;
	}
	public void setNivelConsumoNesquick(double nivelConsumoNesquick) {
		this.nivelConsumoNesquick = nivelConsumoNesquick;
	}

	public double getMaximoTolerancia() {
		return maximoTolerancia;
	}

	public void setMaximoTolerancia(double maximoTolerancia) {
		this.maximoTolerancia = maximoTolerancia;
	}
	
	
	

}
