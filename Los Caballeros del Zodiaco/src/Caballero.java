
public class Caballero {
	private String nombrecaballero;
	private int edadcaballerismo;
	private int nivelmaldadencubierta;
	private Constelacion constelacion;
	
	public String getNombrecaballero() {
		return nombrecaballero;
	}
	public void setNombrecaballero(String nombrecaballero) {
		this.nombrecaballero = nombrecaballero;
	}
	public int getEdadcaballerismo() {
		return edadcaballerismo;
	}
	public void setEdadcaballerismo(int edadcaballerismo) {
		this.edadcaballerismo = edadcaballerismo;
	}
	public int getNivelmaldadencubierta() {
		return nivelmaldadencubierta;
	}
	public void setNivelmaldadencubierta(int nivelmaldadencubierta) {
		this.nivelmaldadencubierta = nivelmaldadencubierta;
	}
	public Constelacion getConstelacion() {
		return constelacion;
	}
	public void setConstelacion(Constelacion constelacion) {
		this.constelacion = constelacion;
	}

}
