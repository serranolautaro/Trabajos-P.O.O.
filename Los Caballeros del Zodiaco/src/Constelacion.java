public class Constelacion {
	private String nombreconstelacion;
	private int distanciasol;
	private boolean enBandaCeleste;
	
	public String getNombreconstelacion() {
		return nombreconstelacion;
	}
	public void setNombreconstelacion(String nombreconstelacion) {
		this.nombreconstelacion = nombreconstelacion;
	}
	public int getDistanciasol() {
		return distanciasol;
	}
	public void setDistanciasol(int distanciasol) {
		this.distanciasol = distanciasol;
	}
	public boolean getEnBandaceleste() {
		return enBandaCeleste;
	}
	public void setBandaceleste(boolean enBandaCeleste) {
		this.enBandaCeleste = enBandaCeleste;
	}
	
	
	
}
